public class demo {
    public static void main(String[] args) {
        //while loop
         int i = 11;
        while(i<=10){
            System.out.println(i);
            i--;
        }


        // do while loop
        
        do{
            System.out.println(i);
            i++;
        }
        while(i<=10);


        //for loop
        for(int j=0; j<=20; j++){
            System.out.println(j);
        }
    }
    
}
